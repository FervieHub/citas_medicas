    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtDescripcion.getText()==""){
            JOptionPane.showMessageDialog(null, "Ingrese la descripci처n del perfil");
        }else{
            String SQLGuardar = "";
            
            Connection con1 = null;
            ConexionBase conectado = new ConexionBase();
            con1 = conectado.getConnection();
            //Statement st = con1.createStatement();
            
            if(transaccion=="Nuevo"){
                SQLGuardar = "INSERT INTO tbl_perfil (perfil_descripcion, perfil_estado) VALUES ('" + this.txtDescripcion.getText().toUpperCase() + "', " + this.chkEstado.isSelected() + ")";
            }else{
                SQLGuardar = "UPDATE tbl_perfil SET perfil_descripcion = '" + this.txtDescripcion.getText().toUpperCase() + "', perfil_estado = " + this.chkEstado.isSelected() + " WHERE id_perfil = " + this.txtId.getText();
            }
            
            try {
                PreparedStatement pst = con1.prepareCall(SQLGuardar);
                
                int n = pst.executeUpdate();
                pst.close();
                
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "Datos guardados correctamente");                   
                    Deshabilitar();
                    CargarPerfil();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar los datos \n" + e);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


