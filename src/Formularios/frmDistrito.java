    public void CargarDistrito(){
        String titulos[] = {"N°", "Id", "Nombre"};
        TablaDistrito = new DefaultTableModel(null, titulos);
        String[] registros = new String[100];
        String SQLBuscar = "Select id, nombre, estado FROM vst_distrito ORDER BY nombre ASC";
        ConexionClinica Conectado = new ConexionClinica();
        con3 = Conectado.getConexion();

        try {
            st = (Statement) con3.createStatement();
            rs = st.executeQuery(SQLBuscar);
            
            int count = 0;
            
            while (rs.next()) {
                count++;
                registros[0] = String.valueOf(count);
                registros[1] = rs.getString("id");
                registros[2] = rs.getString("nombre");
                TablaDistrito.addRow(registros);
            }
            
            tblDistrito.setModel(TablaDistrito);
            
            tblDistrito.getColumnModel().getColumn(0).setPreferredWidth(50);
            
            tblDistrito.getColumnModel().getColumn(1).setMaxWidth(0);
            tblDistrito.getColumnModel().getColumn(1).setMinWidth(0);
            tblDistrito.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(0);
            tblDistrito.getTableHeader().getColumnModel().getColumn(1).setMinWidth(0);
            
            tblDistrito.getColumnModel().getColumn(2).setPreferredWidth(300);
            
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar los datos \n" + e);
        }
    }
    
    public void SearchDistrito(){
        String SQLBuscar = "Select id, nombre, estado FROM vst_distrito WHERE id = " + this.txtId.getText();
        ConexionClinica Conectado = new ConexionClinica();
        con3 = Conectado.getConexion();

        try {
            st = (Statement) con3.createStatement();
            rs = st.executeQuery(SQLBuscar);
            
            int count = 0;
            
            while (rs.next()) {
                txtNombre.setText(rs.getString("nombre"));
                chkEstado.setSelected(rs.getBoolean("estado"));
            }
            
            tblDistrito.setModel(TablaDistrito);
            
            tblDistrito.getColumnModel().getColumn(0).setPreferredWidth(50);
            
            tblDistrito.getColumnModel().getColumn(1).setMaxWidth(0);
            tblDistrito.getColumnModel().getColumn(1).setMinWidth(0);
            tblDistrito.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(0);
            tblDistrito.getTableHeader().getColumnModel().getColumn(1).setMinWidth(0);
            
            tblDistrito.getColumnModel().getColumn(2).setPreferredWidth(300);
            
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar los datos \n" + e);
        }
    }
    
