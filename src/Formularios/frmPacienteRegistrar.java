            String SQLGuardar;
            SQLGuardar = "insert into tbl_personal (paciente_numero,paciente_paterno,paciente_materno,paciente_nombre,id_genero,paciente_direccion,paciente_correo,paciente_telefono) values (?,?,?,?,?,?,?,?)";
